# Cree un programa que tome tres valores por consola multiplique el primero por el segundo 
# y le sume el tercero. Presente el resultado en la terminal del editor
print("El programa solicitara tres valores por consola. Multiplicara el primero por el segundo y luego se le sumara el tercero")

PrimerNumero = int(input("Ingrese el primer numero "))
SegundoNumero = int(input("Ingrese el segundo numero "))
TercerNumero = int(input("Ingrese el tercer numero "))

Resultado = (PrimerNumero*SegundoNumero)+TercerNumero
print("Resultado Final: ")
print(Resultado)
