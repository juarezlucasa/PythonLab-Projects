# Escriba un programa que solicite el radio de una circunferencia y permita calcular
# el perimetro. Presente el resultado en la terminal del editor

RadioCircunferencia = int(input("Ingrese el radio de la circunferencia"))
Pi = 3.1416

Resultado = 2 * Pi * RadioCircunferencia
print("Radio de la circunferencia segun el radio ingresado: ")
print(Resultado)
